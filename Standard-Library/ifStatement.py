x = (input(float('Insira o valor de x')))
y = (input(float('Insira o valor de y')))

if x>y:
    print('x é maior que y')
if x<y:
    print('x é menor que y')
if x>=y:
    print('x é maior ou igual que y')
if x<=y:
    print('x é menor ou igual que y')
if x!=y:
    print('x é diferente de y')
if x==y:
    print('x é igual a y')
